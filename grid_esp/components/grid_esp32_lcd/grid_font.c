int grid_font_draw_string_fast(struct grid_font_model* font, struct grid_gui_model* gui, uint16_t x, uint16_t y, int size, char* string, int* cursor_jump, grid_color_t color) {

  if (gui == NULL) {
    return 1;
  }

  int cursor = 0;

  for (int i = 0; string[i] != 0; i++) {
    grid_font_draw_character_fast(font, gui, x + cursor, y, size, string[i], &cursor, color);
  }

  *cursor_jump = cursor;
  return 0;
}

int grid_font_draw_character_fast(struct grid_font_model* font, struct grid_gui_model* gui, uint16_t x, uint16_t y, int size, int character, int* cursor_jump, grid_color_t color) {

  if (font == NULL) {
    return 1;
  }

  if (gui == NULL) {
    return 1;
  }

  uint8_t scale = (size - 1) / 8 + 1;

  int w = 8 * scale;
  int h = 8 * scale;

  for (int j = 0; j < h; ++j) {
    for (int i = 0; i < w; ++i) {

      if (font8x8_basic[character][j / scale] & (1 << (i / scale))) {
        grid_gui_draw_pixel(gui, x + i, y + j, color);
      } else {
        grid_gui_draw_pixel(gui, x + i, y + j, grid_gui_color_from_rgb(0, 0, 0));
      }
    }

    // putchar('\n');
  }

  *cursor_jump += w;

  return 0;
}
